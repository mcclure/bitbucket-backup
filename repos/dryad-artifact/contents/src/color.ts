// Color functions

export function color(r: number, g: number, b: number) {
    let f = Math.floor
    return "rgb(" + f(r * 255) + "," + f(g * 255) + "," + f(b * 255) + ")"
}

function colorIntArray(ary) {
    let x = "rgb(" + ary[0] + "," + ary[1] + "," + ary[2] + ")"
    return x
}

export function colorHsl(h, s, l) {
    return colorIntArray(hslToRgb(h, s, l))
}

export function colorHsv(h, s, l) {
    return colorIntArray(hsvToRgb(h, s, l))
}

// RGB/HSV from http://mjijackson.com/2008/02/rgb-to-hsl-and-rgb-to-hsv-color-model-conversion-algorithms-in-javascript
